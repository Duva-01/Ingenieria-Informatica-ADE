    public PropiedadDialog(java.awt.Frame parent, boolean modal, Jugador jugador) {
        super(parent, modal);
        initComponents();
        propiedadElegida = -1;
        
        DefaultListModel gestiones = new DefaultListModel<>(); // datos para la lista
        
        ArrayList<String> opciones = new ArrayList<>();
        for(int n=0; n<jugador.getPropiedades().size(); n++)
            opciones.add(jugador.getPropiedades().get(n).getNombre());
        
        for (String s: opciones){
            gestiones.addElement(s);
        } //se completan los datos
        
        listaPropiedades.setModel(gestiones); //se le dice a la lista cuÃ¡les son esos datos
        this.setVisible(true);
    }

